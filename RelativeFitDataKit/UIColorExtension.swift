import UIKit

public extension UIColor {
    public class func fit_emeraldColor() -> UIColor {
        return UIColor(red: 129/255, green: 183/255, blue: 26/255, alpha: 1)
    }

    public class func fit_greyColor() -> UIColor {
        return UIColor(red: 112/255, green: 124/255, blue: 124/255, alpha: 1)
    }
}